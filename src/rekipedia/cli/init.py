_GITATTRIBUTES_LINES = (
    ".rekipedia/wiki/*.md merge=reki-wiki\n"
    "docs/wiki/*.md       merge=reki-wiki\n"
)


def _setup_merge_driver(repo_path: Path) -> None:
    """Write .gitattributes entries and register git merge driver in .git/config."""
    import subprocess

    gitattributes_path = repo_path / ".gitattributes"
    if gitattributes_path.exists():
        existing = gitattributes_path.read_text(encoding="utf-8")
        if "merge=reki-wiki" in existing:
            console.print(
                "[yellow]⚠[/yellow]  .gitattributes already contains merge=reki-wiki entries — skipping."
            )
        else:
            with gitattributes_path.open("a", encoding="utf-8") as fh:
                fh.write(_GITATTRIBUTES_LINES)
            console.print("[green]✔[/green]  Added merge=reki-wiki entries to .gitattributes")
    else:
        gitattributes_path.write_text(_GITATTRIBUTES_LINES, encoding="utf-8")
        console.print("[green]✔[/green]  Created .gitattributes with merge=reki-wiki entries")

    git_config_cmds = [
        ["git", "config", "merge.reki-wiki.name", "rekipedia wiki merge driver"],
        ["git", "config", "merge.reki-wiki.driver", "reki merge-driver %O %A %B"],
        ["git", "config", "merge.reki-wiki.recursive", "binary"],
    ]
    for cmd in git_config_cmds:
        result = subprocess.run(cmd, cwd=repo_path, capture_output=True, text=True)
        if result.returncode != 0:
            console.print(f"[red]✗[/red]  git config failed: {result.stderr.strip()}")
        else:
            console.print(f"[green]✔[/green]  Ran: {' '.join(cmd[2:])}")

    console.print()
    console.print(
        "  [dim]Tip: commit [bold].gitattributes[/bold] so teammates get the merge driver.\n"
        "  [bold].git/config[/bold] is local-only and not committed.[/dim]"
    )


def run_init(  # noqa: C901
    repo_path: Path,
    no_agent_files: bool = False,
    with_ci: bool = False,
    with_merge_driver: bool = False,
    with_upload: str | None = None,
    with_copilot: bool = False,
    with_codex: bool = False,
    with_cursor: bool = False,
    with_all_ai: bool = False,
) -> None:
    if with_all_ai:
        with_copilot = True
        with_codex = True
        with_cursor = True

    wiki_dir = repo_path / ".rekipedia"
    config_path = wiki_dir / "config.yml"
    gitignore_path = repo_path / ".gitignore"

    # Create .rekipedia/ if missing
    wiki_dir.mkdir(parents=True, exist_ok=True)

    # Write config.yml — idempotent (skip if already present)
    if config_path.exists():
        console.print(
            f"[yellow]⚠[/yellow]  [bold]{config_path}[/bold] already exists — skipping."
        )
    else:
        config_path.write_text(
            _DEFAULT_CONFIG_YAML,
            encoding="utf-8",
        )
        console.print(f"[green]✔[/green]  Created [bold]{config_path}[/bold]")

    # Append .gitignore entry — idempotent
    if gitignore_path.exists():
        existing = gitignore_path.read_text(encoding="utf-8")
        if ".rekipedia/store.db" in existing:
            console.print(
                "[yellow]⚠[/yellow]  .gitignore already contains .rekipedia/store.db — skipping."
            )
        else:
            with gitignore_path.open("a", encoding="utf-8") as fh:
                fh.write(_GITIGNORE_ENTRY)
            console.print("[green]✔[/green]  Added .rekipedia/store.db to .gitignore")
    else:
        gitignore_path.write_text(_GITIGNORE_ENTRY, encoding="utf-8")
        console.print("[green]✔[/green]  Created .gitignore with .rekipedia/store.db")

    if not no_agent_files:
        console.print()
        console.print("[bold]Writing agent instruction files…[/bold]")
        _write_agent_files(repo_path)

    if with_ci:
        console.print()
        console.print("[bold]Writing GitHub Actions workflow…[/bold]")
        _write_github_actions(repo_path, with_upload=with_upload)

    if with_merge_driver:
        console.print()
        console.print("[bold]Setting up git merge driver…[/bold]")
        _setup_merge_driver(repo_path)

    if with_copilot:
        console.print()
        console.print("[bold]Writing GitHub Copilot files…[/bold]")
        _write_copilot_files(repo_path)

    if with_codex:
        console.print()
        console.print("[bold]Writing Codex CLI files…[/bold]")
        _write_codex_files(repo_path)

    if with_cursor:
        console.print()
        console.print("[bold]Writing Cursor files…[/bold]")
        _write_cursor_files(repo_path)

    console.print()
    console.print("[bold green]rekipedia initialised.[/bold green]")
    console.print(
        f"  Edit [cyan]{config_path}[/cyan] to choose your LLM provider/model, then run:"
    )
    console.print("  [bold]rekipedia scan .[/bold]")
    if with_ci:
        console.print()
        console.print(
            "  [dim]Tip: set the [bold]REKIPEDIA_API_KEY[/bold] repository secret in GitHub "
            "for LLM-enriched wiki pages. Without it the workflow runs in --no-llm mode.[/dim]"
        )


@click.command("init")
@click.argument(
    "repo",
    default=".",
    type=click.Path(exists=True, file_okay=False, path_type=Path),
)
@click.option(
    "--no-agent-files",
    is_flag=True,
    default=False,
    help="Skip writing CLAUDE.md, AGENTS.md, and .github/copilot-instructions.md.",
)
@click.option(
    "--with-ci",
    is_flag=True,
    default=False,
    help=(
        "Write .github/workflows/rekipedia-wiki.yml — a GitHub Actions workflow that "
        "scans the repo and publishes the wiki to docs/wiki/ on every push to main. "
        "Uses the REKIPEDIA_API_KEY secret when present; falls back to --no-llm otherwise."
    ),
)
@click.option(
    "--with-merge-driver",
    is_flag=True,
    default=False,
    help=(
        "Register reki-wiki as a git merge driver and write .gitattributes entries "
        "for wiki markdown files. .gitattributes should be committed; .git/config is local-only."
    ),
)
@click.option(
    "--with-upload",
    type=click.Choice(["s3", "gcs"]),
    default=None,
    help="Add bundle upload step to CI workflow (s3 or gcs)",
)
@click.option(
    "--with-copilot",
    is_flag=True,
    default=False,
    help="Write .vscode/mcp.json and update .vscode/settings.json for GitHub Copilot.",
)
@click.option(
    "--with-codex",
    is_flag=True,
    default=False,
    help="Write codex-mcp-hint.md and .codex/instructions.md for Codex CLI.",
)
@click.option(
    "--with-cursor",
    is_flag=True,
    default=False,
    help="Write .cursor/mcp.json and .cursor/rules/rekipedia.mdc for Cursor.",
)
@click.option(
    "--with-all-ai",
    is_flag=True,
    default=False,
    help="Enable all AI tool integrations: --with-copilot, --with-codex, --with-cursor.",
)
def init_cmd(
    repo: Path,
    no_agent_files: bool,
    with_ci: bool,
    with_merge_driver: bool,
    with_upload: str | None,
    with_copilot: bool,
    with_codex: bool,
    with_cursor: bool,
    with_all_ai: bool,
) -> None:
    """Initialise rekipedia in REPO (default: current directory)."""
    if with_upload and not with_ci:
        from rich import print as rprint
        rprint("[red]Error:[/] --with-upload requires --with-ci")
        return
    run_init(
        repo.resolve(),
        no_agent_files=no_agent_files,
        with_ci=with_ci,
        with_merge_driver=with_merge_driver,
        with_upload=with_upload,
        with_copilot=with_copilot,
        with_codex=with_codex,
        with_cursor=with_cursor,
        with_all_ai=with_all_ai,
    )
