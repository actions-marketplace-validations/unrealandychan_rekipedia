_GH_ACTIONS_TEMPLATE = """\
# .github/workflows/rekipedia-wiki.yml
# Auto-generated by `reki init --with-ci`
#
# Scans the codebase and publishes a fresh wiki to docs/wiki/ on every push
# to main.  Set the REKIPEDIA_API_KEY repository secret for LLM-enriched pages;
# omit it to run in --no-llm (symbol-only) mode.

name: rekipedia wiki

on:
  push:
    branches: [main]
  workflow_dispatch:

permissions:
  contents: write

jobs:
  wiki:
    runs-on: ubuntu-latest

    steps:
      - name: Checkout repository
        uses: actions/checkout@v4
        with:
          fetch-depth: 0

      - name: Set up Python
        uses: actions/setup-python@v5
        with:
          python-version: "3.11"
          cache: pip

      - name: Install rekipedia
        run: pip install rekipedia

      - name: Scan codebase
        env:
          REKIPEDIA_API_KEY: ${{ secrets.REKIPEDIA_API_KEY }}
        run: |
          if [ -n "$REKIPEDIA_API_KEY" ]; then
            reki scan .
          else
            echo "REKIPEDIA_API_KEY not set — running in --no-llm mode"
            reki scan . --no-llm
          fi

      - name: Publish wiki to docs/wiki/
        run: |
          mkdir -p docs/wiki
          reki publish . --output-dir docs/wiki/ --force

      - name: Commit and push wiki changes
        run: |
          git config user.name  "github-actions[bot]"
          git config user.email "github-actions[bot]@users.noreply.github.com"
          git add docs/wiki/
          if git diff --cached --quiet; then
            echo "No wiki changes — nothing to commit."
          else
            git commit -m "chore: update rekipedia wiki [skip ci]"
            git push
"""

